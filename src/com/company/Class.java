package com.company;

public class Class {
    private int number;
    private String soz;
    private int [] massive;


    public Class (int number, String soz, int massive[]){
        this.number = number;
        this.soz = soz;
        this.massive = massive;
    }

    public int getNumber() {
        return number;
    }

    public void setNumber(int number){
        this.number = number;
    }
    public String getSoz(){
        return soz;
    }
    public void setSoz(String soz){
        this.soz = soz;
    }
    public int [] getMassive(){
        return massive;
    }
    public void setMassive(int[] massive){
        this.massive = massive;
    }





}



